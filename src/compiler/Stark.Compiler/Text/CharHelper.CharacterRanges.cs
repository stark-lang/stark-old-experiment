        private struct CharacterRange
        {
            public CharacterRange(char32 start, char32 end)
            {
                Start = start;
                End = end;
            }

            public readonly char32 Start;

            public readonly char32 End;

            public int CompareTo(int c)
            {
                if (c < Start) return 1;
                if (c > End) return -1;
                return 0;
            }
